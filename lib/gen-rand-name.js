// i hate how arrays don't have to be stated as mutable but whatever
const names = namesFull;

export function getRandName() {
    if (names.length > 0) {
        const index = Math.floor(Math.random() * names.length);
        const newName = names[index];
        // remove the name to stop duplicates because getByName needs unique ids
        // and yes i hate using an array for this
        names.splice(index, 1);
        return newName;
    } else {
        // pray to the math.rand gods for a unique id
        return Math.floor(Math.random() * 100000);
    }
}
